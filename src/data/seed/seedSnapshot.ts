function link(
  id: string,
  source: string,
  target: string,
  total: number,
  strength: SynergyLink['synergyStrength'],
  notes?: string
): SynergyLink {
  return {
    id,
    sourceIdeaId: source,
    targetIdeaId: target,
    sameAudienceScore: Math.min(10, total),
    sameBrandUniverseScore: Math.min(10, total - 1),
    sameCapabilitiesScore: Math.min(10, total),
    totalSynergyScore: total,
    synergyStrength: strength,
    notes,
    createdAt: now,
    updatedAt: now,
  }
}

